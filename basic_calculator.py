num1 = int(input("enter the first number  : "))
op   = input("enter the operation     : ")
num2 = int(input("enter the second number : "))
if (op == "+"):
	result = num1+num2
	print(result)
if (op == "-"):
	result = num1-num2
	print(result)
if (op == "*"):
	result = num1*num2
	print(result)
if (op == "/"):
	result = num1/num2
	print(result)
 